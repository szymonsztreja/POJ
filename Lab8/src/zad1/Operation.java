package zad1;

public class Operation {
    public static int add(int first, int second) {
        return first+second;
    }

    public static int sub(int first, int second) {
        return first - second;
    }

    public static int mul(int first, int second) {
        return first * second;
    }

    public static int div(int first, int second) {
        return first / second;
    }
}
